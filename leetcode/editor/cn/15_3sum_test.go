func threeSum(nums []int) [][]int {
	var res [][]int
	sort.Ints(nums)

	for i := 0; i < len(nums) - 2; i++ {
		if nums[i] > 0 {
			break
		}

		if i > 0 && nums[i] == nums[i - 1] {
			continue
		}

		l, r, n1 := i + 1, len(nums) - 1, nums[i]
		for l < r {
			n2, n3 := nums[l], nums[r]
			if n1 + n2 + n3 == 0 {
				res = append(res, []int{n1, n2, n3})
				for l < r && nums[l] == n2 {
					l++
				}

				for l < r && nums[r] == n3 {
					r--
				}
			} else if n1 + n2 + n3 < 0 {
				l++
			} else {
				r--
			}
		}
	}

	return res
}
